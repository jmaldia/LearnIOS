#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
//#warning Potentially incomplete method implementation.
    // Return the number of sections.
    return [dictTable.allKeys count];
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
//#warning Incomplete method implementation.
    // Return the number of rows in the section.
    NSString *key = [dictTable.allKeys objectAtIndex:section];
    
    NSArray *keyValues = (NSArray *)[dictTable objectForKey:key];
    
    return [keyValues count];
}

- (NSString *)tableView:(UITableView *)tableView titleForHeaderInSection:(NSInteger)section   // fixed font style. use custom view (UILabel) if you want something different
{
    NSString *key = [dictTable.allKeys objectAtIndex:section];
    
    return key;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"Cell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:CellIdentifier] autorelease];
    }
    
    // Configure the cell...
    NSString *key = [dictTable.allKeys objectAtIndex:indexPath.section];
    
    NSArray *keyValues = (NSArray *)[dictTable objectForKey:key];
    
    
    cell.textLabel.text = [keyValues objectAtIndex:indexPath.row];     
    
    
    return cell;
}

